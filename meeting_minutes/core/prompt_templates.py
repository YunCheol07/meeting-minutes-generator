"""프롬프트 템플릿 모듈 - 각 처리 단계별 프롬프트 정의"""


class PromptTemplates:
    """회의록 생성을 위한 프롬프트 템플릿 모음
    
    각 노드에서 사용할 프롬프트를 정적 메서드로 제공합니다.
    """
    
    @staticmethod
    def get_preprocessing_prompt() -> str:
        """전처리 프롬프트"""
        return """다음 회의 대화 내용을 정리하여 구조화된 형태로 변환하세요.

대화 내용:
{transcript}

작업 지침:
1. 불필요한 반복이나 군더더기 제거
2. 문장 정리 및 맞춤법 교정
3. 화자별 발언 정리 (가능한 경우)
4. 시간 순서대로 정리

정리된 내용만 반환하세요. 추가 설명이나 주석은 불필요합니다."""

    @staticmethod
    def get_participant_extraction_prompt() -> str:
        """참석자 추출 프롬프트"""
        return """다음 회의 내용에서 참석자 이름을 추출하세요.

회의 내용:
{text}

지침:
- 명확하게 언급된 사람의 이름만 추출
- 직급, 부서, 호칭은 제외하고 이름만 추출
- 쉼표(,)로 구분하여 한 줄로 나열
- 예시: 김철수, 이영희, 박민수

참석자 목록:"""

    @staticmethod
    def get_summary_prompt() -> str:
        """요약 프롬프트"""
        return """다음 회의 내용을 간결하게 요약하세요.

회의 내용:
{text}

요구사항:
- 주요 논의 사항 중심으로 요약
- 3~5문장으로 간결하게 작성
- 핵심 내용과 결론 위주로 정리
- 객관적이고 사실적인 톤 유지

요약:"""

    @staticmethod
    def get_agenda_extraction_prompt() -> str:
        """안건 추출 프롬프트"""
        return """다음 회의 내용에서 논의된 안건 항목들을 추출하세요.

회의 내용:
{text}

형식:
각 안건을 한 줄씩, 번호나 기호 없이 나열하세요.

예시:
신규 프로젝트 진행 계획
예산 승인 건
일정 조율

안건 목록:"""

    @staticmethod
    def get_discussion_extraction_prompt() -> str:
        """논의 내용 추출 프롬프트"""
        return """다음 회의 내용에서 각 안건별 논의 내용을 추출하세요.

회의 내용:
{text}

출력 형식 (JSON):
각 논의 내용을 다음 형식의 JSON으로 작성하고, 한 줄에 하나씩 나열하세요.
{{"topic": "논의 주제", "content": "논의 내용 요약"}}

예시:
{{"topic": "프로젝트 일정", "content": "11월 말까지 개발 완료 목표로 진행하기로 합의"}}
{{"topic": "예산 배정", "content": "마케팅 예산 500만원 추가 승인"}}

논의 내용:"""

    @staticmethod
    def get_decision_extraction_prompt() -> str:
        """결정 사항 추출 프롬프트"""
        return """다음 회의 내용에서 합의되거나 결정된 사항을 추출하세요.

회의 내용:
{text}

지침:
- 명확하게 결정된 사항만 추출
- 각 결정 사항을 한 줄씩 나열
- 구체적인 내용 포함 (날짜, 금액, 담당자 등)
- 번호나 기호 없이 작성

예시:
프로젝트 마감일을 12월 31일로 확정
마케팅 예산 500만원 승인

결정 사항:"""

    @staticmethod
    def get_action_item_extraction_prompt() -> str:
        """액션 아이템 추출 프롬프트"""
        return """다음 회의 내용에서 액션 아이템(실행 과제)을 추출하세요.

회의 내용:
{text}

출력 형식 (JSON):
각 액션 아이템을 다음 형식의 JSON으로 작성하고, 한 줄에 하나씩 나열하세요.
{{"task": "작업 내용", "assignee": "담당자", "deadline": "마감일"}}

지침:
- 마감일이 명시되지 않은 경우 "미정"으로 표시
- 담당자가 없으면 "미지정"으로 표시
- 작업 내용은 구체적으로 작성

예시:
{{"task": "프로토타입 개발", "assignee": "김철수", "deadline": "2025-11-15"}}
{{"task": "테스트 시나리오 작성", "assignee": "이영희", "deadline": "2025-11-20"}}

액션 아이템:"""
